package JavaIntro;

public class Sample_Operators {

	public static void main(String[] args) {
		// Compound Assign Operators
		
		int i=10; int j=20;
		i+=j;               //>>>>>>>>>>>>>>>>>>>>>>>>> i=i+j;
		System.out.println(i);
		
		i-=j;
		System.out.println(i);
		
		i*=j;
		System.out.println(i);
		
		

	}

}
